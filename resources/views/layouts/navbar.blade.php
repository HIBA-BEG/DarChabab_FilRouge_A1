    copyPhoneNumberBtn.addEventListener('click', function(event) {
        event.preventDefault();

        // Create a dummy textarea element to hold the phone number
        var dummyTextArea = document.createElement('textarea');
        dummyTextArea.value = phoneNumber;

        // Append the textarea to the body
        document.body.appendChild(dummyTextArea);

        // Select the phone number inside the textarea
        dummyTextArea.select();
        dummyTextArea.setSelectionRange(0, 99999); // For mobile devices

        // Copy the selected text
        document.execCommand('copy');

        // Remove the textarea from the DOM
        document.body.removeChild(dummyTextArea);

        // Show a message to the user
        alert('Phone number copied: ' + phoneNumber);
    });
});

